package testing.Interfaces;

import java.util.ArrayList;

/**
 * Интерфейс модели игры для интеграции в архитектуру MVC.
 * <p>
 * Выступает в роли Subject (наблюдаемый объект) в паттерне "Наблюдатель"
 * для взаимодействия между контроллером и представлением.
 * </p>
 * <p>
 * Хранит текущее состояние игрового поля, позицию пустой клетки и флаг победы.
 * </p>
 *
 * @author Daniil-Melnik
 * @version 1.0
 * @see HolderObserver
 * @see HolderChangePublisher
 */
public interface Holder {

    /**
     * Возвращает текущее состояние игрового поля.
     *
     * @return ArrayList, содержащий значения всех клеток поля (9 элементов)
     */
    ArrayList<String> getField();

    /**
     * Возвращает позицию пустой клетки.
     *
     * @return индекс пустой клетки (*) в массиве поля (0-8)
     */
    int getZeroPosition();

    /**
     * Проверяет, выиграна ли игра.
     *
     * @return true, если текущее состояние поля соответствует выигрышному, иначе false
     */
    boolean isWin();

    /**
     * Обновляет состояние модели игры.
     * <p>
     * После обновления всех полей автоматически уведомляет всех подписанных
     * наблюдателей об изменении состояния.
     * </p>
     *
     * @param newField      новое состояние игрового поля
     * @param newZeroPosition новая позиция пустой клетки (-1 для автоматического поиска)
     * @param win           новый флаг победы
     */
    void updateHolder(ArrayList<String> newField, int newZeroPosition, boolean win);

    /**
     * Возвращает ссылку на текущий экземпляр Holder.
     * <p>
     * Используется наблюдателями для получения актуального состояния модели.
     * </p>
     *
     * @return текущий экземпляр Holder
     */
    Holder getHolder();
}