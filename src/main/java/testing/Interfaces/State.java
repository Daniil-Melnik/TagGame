package testing.Interfaces;

import testing.GameBackend.DTO.InDTO;

/**
 * Интерфейс, описывающий поведение контроллера в соответствии с паттерном "Состояние".
 * <p>
 * Различные реализации этого интерфейса определяют разное поведение
 * контроллера в зависимости от текущего состояния игры (активная игра, победа и т.д.).
 * </p>
 *
 * @author Daniil-Melnik
 * @version 1.0
 */
public interface State {

    /**
     * Выполняет ход в игре.
     * <p>
     * Реализация этого метода зависит от текущего состояния:
     * <ul>
     *     <li>В состоянии "Игра идет" - ход выполняется</li>
     *     <li>В состоянии "Победа" - ход блокируется с выводом сообщения</li>
     * </ul>
     * </p>
     *
     * @param inDTO DTO с параметрами хода (позиция и значение кнопки)
     */
    void makeMove(InDTO inDTO);
}